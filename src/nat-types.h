#ifndef _NAT_TYPES_H_
#define _NAT_TYPES_H_

#include <stdint.h>
  
/* Signed Integer */

typedef char    i8;
typedef int16_t i16;
typedef int32_t i32;
typedef int64_t i64;

/* Unsigned Integer */

typedef unsigned char u8;
typedef uint16_t      u16;
typedef uint32_t      u32;
typedef uint64_t      u64;

/* Floating Point */

typedef float  f32;
typedef double f64;

/* Boolean */

typedef i8 bool;

#endif /* #ifndef _NAT_TYPES_H_ */
